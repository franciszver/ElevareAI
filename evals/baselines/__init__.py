"""Committed baseline snapshot + load/save helpers (Phase E4).

`baseline.json` is the DETERMINISTIC + latency reference used by
`evals/regression.py::compare_to_baseline` to gate later runs, per the
locked plan in `evals/README.md`: "fail if a surface pass-rate drops >10
points OR p95 latency >1.5x baseline". It is generated by grading the real
captured fixtures (`evals/fixtures/sample_outputs.json` +
`evals/fixtures/guardrail_outputs.json`) offline via
`evals/grade_fixtures.py::grade_fixture_file` — no live API call.

Judge (LLM-as-judge quality) scores are intentionally NOT part of this
snapshot or the regression gate: re-judging needs a live model/subagent
judge, which isn't available in an offline/CI context. Judge scores stay
advisory, reported separately (see `evals/report.py::build_judge_report`).

To regenerate the baseline after an intentional behavior change, run:
    python -m evals.run_eval --update-baseline
"""

import json
from pathlib import Path
from typing import Any, Dict, List

from evals.grade_fixtures import (
    DEFAULT_FIXTURES,
    GUARDRAIL_FIXTURES,
    grade_fixture_files,
)
from evals.report import build_perf_report, build_report

SCHEMA_VERSION = 1

BASELINE_PATH = Path(__file__).parent / "baseline.json"

# The model the captured fixtures came from. Not embedded as a wall-clock
# timestamp (see module/CLAUDE.md guidance) - a hardcoded known capture date
# string is used instead of datetime.now().
BASELINE_MODEL = "openai/gpt-oss-20b:free"
BASELINE_CAPTURE_DATE = "2026-07-16"


def build_baseline(
    fixtures: List[Path] = None,
    model: str = BASELINE_MODEL,
    capture_date: str = BASELINE_CAPTURE_DATE,
) -> Dict[str, Any]:
    """Grade `fixtures` (default: sample_outputs.json + guardrail_outputs.json)
    offline and assemble the baseline snapshot dict: per-surface
    deterministic pass_rate/mean_score + latency p50/p95/mean_tokens."""
    fixtures = (
        fixtures if fixtures is not None else [DEFAULT_FIXTURES, GUARDRAIL_FIXTURES]
    )

    results = grade_fixture_files(fixtures)

    report = build_report(results)
    perf_report = build_perf_report(results)

    surfaces: Dict[str, Any] = {}
    for surface in sorted(set(report) | set(perf_report)):
        surfaces[surface] = {
            "deterministic": report.get(surface, {}),
            "perf": perf_report.get(surface, {}),
        }

    return {
        "schema_version": SCHEMA_VERSION,
        "captured_from": {
            # Stored relative to evals/ (not absolute paths) so the baseline
            # is portable across machines/CI.
            "fixtures": [f"evals/fixtures/{Path(p).name}" for p in fixtures],
            "model": model,
            "date": capture_date,
        },
        "note": (
            "Deterministic + latency reference only. Judge scores are "
            "advisory (not part of the hard regression gate) since "
            "re-judging requires a live model/subagent judge."
        ),
        "surfaces": surfaces,
    }


def save_baseline(baseline: Dict[str, Any], path: Path = BASELINE_PATH) -> None:
    with Path(path).open("w", encoding="utf-8") as f:
        json.dump(baseline, f, indent=2, sort_keys=True)
        f.write("\n")


def load_baseline(path: Path = BASELINE_PATH) -> Dict[str, Any]:
    with Path(path).open(encoding="utf-8") as f:
        return json.load(f)


def snapshot_baseline(path: Path = BASELINE_PATH, **build_kwargs) -> Dict[str, Any]:
    """Regenerate and write the baseline snapshot to `path`. Returns the
    written dict. The `--update-baseline` entry point in `evals/run_eval.py`
    calls this."""
    baseline = build_baseline(**build_kwargs)
    save_baseline(baseline, path)
    return baseline
